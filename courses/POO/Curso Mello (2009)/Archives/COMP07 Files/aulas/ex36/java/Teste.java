public class Teste {
	private int a;

	public Teste() { this.a = 10; }
	public Teste(int a) { this.a = a; }

	public int soma(int a, int b) { return a + b; }
	public double soma(int a, double b) { return a + b; }

	public int getA() { return this.a; }

	public static void main(String args[]) {
		Teste t = new Teste();
		System.out.println(t.getA());

		Teste t2 = new Teste(15);
		System.out.println(t2.getA());

		System.out.println(t.soma(5, 4));
		System.out.println(t.soma(5, 4.1));
	}
}
